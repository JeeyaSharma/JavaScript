// Math is a Built in Object in Js which is used to perform many mathematical operations

console.log(Math.PI); //gives value of PI

console.log(Math.max(10,20,30,34,21,12,5));//gives maximum number

console.log(Math.min(10,20,30,34,21,12,5));//gives minimum number

console.log(Math.round(1.5));
// For Math.round, if the value is greater than or equal n.5 then it rounded off the next number and if it is smaller then it is rounded to the previous integer

console.log(Math.floor(1.8)); //gives the next greater integer

console.log(Math.ceil(1.1)); //gives the next smaller integer

console.log(Math.abs(-5)); //always gives positive value

console.log(Math.random()); //gives random value between 0 and 1

console.log(Math.sqrt(25));//square root of a number

console.log(Math.pow(2,5));//gives exponent times base value

console.log(Math.E);//gives value of Euler's constant

console.log(Math.LN2);// calculates natural log of 2

console.log(Math.LN10);//calculates natural log of 10

console.log(Math.LOG2E);//calculates log Base-2 of E

console.log(Math.LOG10E);//calculates log Base-10 of E

console.log(Math.sin(90));//returns sin of angle 'x' in radians

console.log(Math.cos(45));//returns cos of angle 'x' in radians

console.log(Math.tan(60));//returns tan of angle 'x' in radians